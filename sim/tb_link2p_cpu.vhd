   -- bind the internal cpu state records now that both cores exist
   monitor_bind : block
   begin
      c1_export <= << signal .tb_link2p_cpu.icore1.cpu_export : work.pexport.cpu_export_type >>;
      c2_export <= << signal .tb_link2p_cpu.icore2.cpu_export : work.pexport.cpu_export_type >>;
   end block;

   -- ==================================================================
   -- SIO wire decoder: Multi-Player mode sends 16 bits LSB-first on SD,
   -- start-gated by SC going low (busy line held by whichever end is
   -- driving). We just watch for SD's falling edge (start bit) then sample
   -- 16 bits at the fastest configured baud (145 ce ticks = 115200) --
   -- LinkCable.hpp always configures the fastest baud, matching
   -- sim/tb_link.vhd test 5's replay of its exact register choreography.
   -- ==================================================================
   sio_decode : process
      variable bits    : std_logic_vector(15 downto 0);
      variable k       : integer;
      variable cecount : integer;
   begin
      wait until GBA_on = '1';
      loop
         wait until falling_edge(l_sd);
         for k in 0 to 15 loop
            wait for 145 * CLK_PERIOD; -- one bit period at 115200 baud
            bits(k) := l_sd;
         end loop;
         sio_frame_count <= sio_frame_count + 1;
         report "SIO frame #" & integer'image(sio_frame_count) &
                " @ " & time'image(now) & " : 0x" &
                to_hstring(bits);
      end loop;
   end process;

   -- ==================================================================
   -- run control / progress trace
   -- ==================================================================
   progress : process
   begin
      wait until GBA_on = '1';
      report "reset released, cores running";
      loop
         wait until rising_edge(c1_vblank) for 50 ms;
         exit when not (c1_vblank'event);
         report "core1 vblank, pc=0x" & to_hstring(c1_export.pc) &
                "  core2 pc=0x" & to_hstring(c2_export.pc);
      end loop;
      report "progress watchdog: no core1 vblank within timeout window" severity warning;
      wait;
   end process;

   -- catch the first metavalue reaching either program counter, with the
   -- last known-good pc for orientation
   xwatch : process (clk)
      variable c1_lastpc, c2_lastpc : unsigned(31 downto 0) := (others => '0');
      variable tripped              : boolean := false;
   begin
      if rising_edge(clk) and GBA_on = '1' and not tripped then
         if is_x(std_logic_vector(c1_export.pc)) or is_x(std_logic_vector(c2_export.pc)) then
            report "X reached cpu pc: core1=0x" & to_hstring(c1_export.pc) &
                   " (last 0x" & to_hstring(c1_lastpc) & ")" &
                   "  core2=0x" & to_hstring(c2_export.pc) &
                   " (last 0x" & to_hstring(c2_lastpc) & ")"
               severity error;
            tripped := true;
         else
            c1_lastpc := c1_export.pc;
            c2_lastpc := c2_export.pc;
         end if;
      end if;
   end process;

   -- coarse PC heartbeat so a hung boot is diagnosable without a wave dump
   heartbeat : process
   begin
      wait until GBA_on = '1';
      loop
         wait for 250 us;
         report "pc trace: core1=0x" & to_hstring(c1_export.pc) &
                "  core2=0x" & to_hstring(c2_export.pc);
      end loop;
   end process;

   -- see the declaration comment: dump each finished core1 frame as an
   -- ASCII PGM (grayscale = summed RGB555 channels) so the demo's screen
   -- text is directly readable from the sim output directory
   framedump : process (clk)
      type t_fb is array (0 to 159, 0 to 239) of integer range 0 to 93;
      variable fb        : t_fb;
      variable frame_n   : integer := 0;
      variable seen_px   : boolean := false;
      file     fout      : text;
      variable l         : line;
   begin
      if rising_edge(clk) then
         if (c1_px_we = '1') then
            fb(c1_px_y, c1_px_x) :=
               to_integer(unsigned(c1_px_data(14 downto 10))) +
               to_integer(unsigned(c1_px_data( 9 downto  5))) +
               to_integer(unsigned(c1_px_data( 4 downto  0)));
            seen_px := true;
            if (c1_px_y = 159 and c1_px_x = 239) then
               file_open(fout, "sim/tests/frame_c1_" & integer'image(frame_n) & ".pgm", write_mode);
               write(l, string'("P2")); writeline(fout, l);
               write(l, string'("240 160")); writeline(fout, l);
               write(l, string'("93")); writeline(fout, l);
               for y in 0 to 159 loop
                  for x in 0 to 239 loop
                     write(l, integer'image(fb(y, x)));
                     if (x /= 239) then write(l, string'(" ")); end if;
                  end loop;
                  writeline(fout, l);
               end loop;
               file_close(fout);
               report "frame_c1_" & integer'image(frame_n) & ".pgm written";
               frame_n := frame_n + 1;
            end if;
         end if;
      end if;
   end process;

end architecture;
