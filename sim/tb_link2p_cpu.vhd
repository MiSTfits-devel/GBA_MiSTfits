   );

   icore2 : entity work.gba_top
   generic map
   (
      Softmap_GBA_Gamerom_ADDR => 0,
      Softmap_GBA_FLASH_ADDR   => 0,
      Softmap_GBA_EEPROM_ADDR  => 0,
