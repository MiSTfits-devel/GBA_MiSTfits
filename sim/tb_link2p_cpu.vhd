-- CPU-level link-cable confidence check: boots the real homebrew demo
-- sim/tests/LinkCable_basic.gba (afska/gba-link-connection v8.0.3) on two
-- gba_top cores wired cable-to-cable exactly like the internal 2P link
-- (rtl/gba_wrap.vhd ~975-993: wired-AND SC/SD, crossed SO/SI, registered on
-- clk1x), each fed by a behavioral cart model reading the same ROM file and
-- a real 16KB BIOS image (sim/tests/bios_words.hex, generated by the run
-- script from $GBA_BIOS -- use a fast-boot BIOS to skip the logo animation).
--
-- Pass criterion: decode the Multi-Player SIO frames on the shared SD line
-- and show both cores' demo counters actually incrementing and flowing in
-- both directions -- not just that registers toggle.
--
-- run: sim/run_link2p_cpu_tb.sh

entity tb_link2p_cpu is
end entity;

architecture sim of tb_link2p_cpu is

   constant CLK_PERIOD : time := 10 ns; -- 100 MHz sim clock == gba_top's clk1x

   signal clk  : std_logic := '0';
   signal done : boolean   := false;

   -- ------------------------------------------------------------------
   -- BIOS stub, loaded into both cores identically via bios_wr* at time 0
   -- ------------------------------------------------------------------
   constant BIOS_WORDS : integer := 4096; -- full 16KB BIOS

   type t_word_array is array (natural range <>) of std_logic_vector(31 downto 0);

   impure function load_hexfile(path : string; max_words : integer) return t_word_array is
      file     f       : text;
      variable ln      : line;
      variable words   : t_word_array(0 to max_words - 1) := (others => (others => '0'));
      variable i       : integer := 0;
      variable hexword : std_logic_vector(31 downto 0);
      variable ok      : boolean;
   begin
      file_open(f, path, read_mode);
      while not endfile(f) and i < max_words loop
         readline(f, ln);
         hread(ln, hexword, ok);
         assert ok report "hread failed on " & path severity failure;
         words(i) := hexword;
         i := i + 1;
      end loop;
      file_close(f);
      report path & ": loaded " & integer'image(i) & " words";
      return words;
   end function;

   -- ------------------------------------------------------------------
   -- cart ROM, shared image loaded once, served independently to each core
   -- ------------------------------------------------------------------
   constant ROM_WORDS : integer := 9958; -- LinkCable_basic.gba size / 4

   shared variable rom_image  : t_word_array(0 to ROM_WORDS - 1);
   shared variable bios_image : t_word_array(0 to BIOS_WORDS - 1);

   -- ------------------------------------------------------------------
   -- per-core signals
   -- ------------------------------------------------------------------
   type t_core_io is record
      cart_ena       : std_logic;
      cart_rnw       : std_logic;
      cart_addr      : std_logic_vector(27 downto 0);
      cart_writedata : std_logic_vector(7 downto 0);
      cart_done      : std_logic;
      cart_readdata  : std_logic_vector(31 downto 0);
   end record;

   signal c1, c2 : t_core_io;

   signal bios_wraddr : std_logic_vector(11 downto 0) := (others => '0');
   signal bios_wrdata : std_logic_vector(31 downto 0) := (others => '0');
   signal bios_wr     : std_logic := '0';

   signal GBA_on : std_logic := '0';

   -- link port signals, (value, oe) pairs per core, cross-wired like
   -- gba_wrap.vhd's internal 2P link (core1 = parent)
   signal c1_link_clk_out, c1_link_clk_oe, c1_link_clk_in : std_logic;
   signal c1_link_so_out,  c1_link_so_oe,  c1_link_si_in  : std_logic;
   signal c1_link_sd_out,  c1_link_sd_oe,  c1_link_sd_in  : std_logic;

   signal c2_link_clk_out, c2_link_clk_oe, c2_link_clk_in : std_logic;
   signal c2_link_so_out,  c2_link_so_oe,  c2_link_si_in  : std_logic;
   signal c2_link_sd_out,  c2_link_sd_oe,  c2_link_sd_in  : std_logic;

   signal l_sc, l_sd, l_si_c1, l_si_c2 : std_logic := '1';

   signal c1_vblank, c2_vblank : std_logic;
   signal c1_pc, c2_pc         : std_logic_vector(31 downto 0);

   -- real CPU state, pulled from inside each core (VHDL-2008 external names,
   -- resolved in the monitor block below after the cores have elaborated;
   -- the debug_cpu_pc port is only driven by gba_wrap, not gba_top)
   signal c1_export, c2_export : work.pexport.cpu_export_type;

   constant ZERO28 : std_logic_vector(27 downto 0) := (others => '0');
   constant ZERO32 : std_logic_vector(31 downto 0) := (others => '0');
   constant ZERO64 : std_logic_vector(63 downto 0) := (others => '0');

   -- ------------------------------------------------------------------
   -- SIO wire decoder: watches the shared SD line for Multi-Player frames
   -- and reconstructs the 16-bit words each end actually put on the wire,
   -- the same LSB-first sampling technique sim/tb_link.vhd's cap_bits
   -- process uses, but free-running (captures every frame, not just an
   -- armed one-shot) and printed via `report` for a human-readable trace.
   -- ------------------------------------------------------------------
   signal sio_frame_count : integer := 0;

   -- ------------------------------------------------------------------
   -- framebuffer dump: core1's rendered frames written as ASCII PGM so the
   -- demo's on-screen text ("Players: N") can be read directly from the
   -- sim, closing the interpretation gap between wire traces and what the
   -- software actually displays. One file per frame, grayscale from RGB555.
   -- ------------------------------------------------------------------
   signal c1_px_x    : integer range 0 to 239;
   signal c1_px_y    : integer range 0 to 159;
   signal c1_px_data : std_logic_vector(14 downto 0);
   signal c1_px_we   : std_logic;

begin

   clk <= not clk after CLK_PERIOD / 2 when not done else '0';

   -- ==================================================================
   -- BIOS + ROM image loading (simulation-time only, not synthesizable)
   -- ==================================================================
   preload : process
   begin
      rom_image  := load_hexfile("sim/tests/rom_words.hex", ROM_WORDS);
      bios_image := load_hexfile("sim/tests/bios_words.hex", BIOS_WORDS);
      wait;
   end process;

   -- drive bios_wr* once at the start for both cores (shared bus, both
   -- gba_top instances see the same bios_wraddr/bios_wrdata/bios_wr)
   biosload : process
      variable i : integer;
   begin
      wait for 100 ns;
      wait until rising_edge(clk);
      for i in 0 to BIOS_WORDS - 1 loop
         bios_wraddr <= std_logic_vector(to_unsigned(i, 12));
         bios_wrdata <= bios_image(i);
         bios_wr     <= '1';
         wait until rising_edge(clk);
      end loop;
      bios_wr <= '0';
      report "BIOS loaded into both cores";

      -- power-on reset after BIOS is in place
      wait for 100 ns;
      GBA_on <= '1';
      wait;
   end process;

   -- ==================================================================
   -- behavioral cart models: serve 32-bit words from rom_image, modeling
   -- a fixed few-cycle latency like real cart_done handshaking (waitstates
   -- inside gba_memorymux are layered on top of this independently)
   -- ==================================================================
   cart1 : process (clk)
      variable idx : integer;
   begin
      if rising_edge(clk) then
         c1.cart_done <= '0';
         if (c1.cart_ena = '1') then
            idx := to_integer(unsigned(c1.cart_addr(23 downto 2)));
            if (idx < ROM_WORDS) then
               c1.cart_readdata <= rom_image(idx);
            else
               c1.cart_readdata <= x"FFFFFFFF"; -- open bus past ROM end
            end if;
            c1.cart_done <= '1';
         end if;
      end if;
   end process;

   cart2 : process (clk)
      variable idx : integer;
   begin
      if rising_edge(clk) then
         c2.cart_done <= '0';
         if (c2.cart_ena = '1') then
            idx := to_integer(unsigned(c2.cart_addr(23 downto 2)));
            if (idx < ROM_WORDS) then
               c2.cart_readdata <= rom_image(idx);
            else
               c2.cart_readdata <= x"FFFFFFFF";
            end if;
            c2.cart_done <= '1';
         end if;
      end if;
   end process;

   -- ==================================================================
   -- cross-wired internal link cable: identical pattern to
   -- rtl/gba_wrap.vhd's registered wired-AND SC/SD + crossed SO/SI
   -- (core 1 = parent, core 2 = child)
   -- ==================================================================
   process (clk)
   begin
      if rising_edge(clk) then
         l_sc    <= (c1_link_clk_out or not c1_link_clk_oe) and (c2_link_clk_out or not c2_link_clk_oe);
         l_sd    <= (c1_link_sd_out  or not c1_link_sd_oe ) and (c2_link_sd_out  or not c2_link_sd_oe );
         l_si_c1 <= '0'; -- real cable: 1P plug grounds SI (that's what makes core 1 the master)
         l_si_c2 <= (c1_link_so_out  or not c1_link_so_oe );
      end if;
   end process;

   c1_link_clk_in <= l_sc;
   c1_link_si_in  <= l_si_c1;
   c1_link_sd_in  <= l_sd;
   c2_link_clk_in <= l_sc;
   c2_link_si_in  <= l_si_c2;
   c2_link_sd_in  <= l_sd;

   -- both ends actively driving SD low at once is a protocol violation,
   -- exactly the standing assertion sim/tb_link.vhd carries
   process (clk)
   begin
      if rising_edge(clk) then
         assert not (c1_link_sd_oe = '1' and c1_link_sd_out = '0' and
                     c2_link_sd_oe = '1' and c2_link_sd_out = '0')
            report "SD driven low by both cores simultaneously" severity failure;
      end if;
   end process;

   -- ==================================================================
   -- two gba_top cores
   -- ==================================================================
   icore1 : entity work.gba_top
   generic map
