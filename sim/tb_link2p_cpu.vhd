      Softmap_Rewind_ADDR      => 0,
      is_simu                  => '1',
      simu_export_trace        => '0',
      strip_savestates         => '1',
      strip_cheats             => '1',
      ewram_in_sdram           => '0',
      turbosound               => '0'
   )
   port map
   (
      clk1x                 => clk,
      GBA_on                => GBA_on,
      pause                 => '0',
      allowUnpause          => '1',
      inPause               => open,
      GBA_lockspeed         => '1',
      GBA_cputurbo          => '0',
      GBA_flash_1m          => '0',
      Underclock            => "00",
      CyclesMissing         => open,
      CyclesVsyncSpeed      => open,
      increaseSSHeaderCount => '0',
      save_state            => '0',
      load_state            => '0',
      interframe_blend      => "00",
      shade_mode            => "000",
      rewind_on             => '0',
      rewind_active         => '0',
      savestate_number      => 0,

      error_cpu             => open,
      error_memRequ_timeout => open,
      error_memResp_timeout => open,
      flash_busy            => '0',

      cheat_clear           => '0',
      cheats_enabled        => '0',
      cheat_on              => '0',
      cheat_in              => (others => '0'),
      cheats_active         => open,

      cart_ena              => c1.cart_ena,
      cart_idle             => open,
      cart_32               => open,
      cart_rnw              => c1.cart_rnw,
      cart_addr             => c1.cart_addr,
      cart_writedata        => c1.cart_writedata,
      cart_done             => c1.cart_done,
      cart_readdata         => c1.cart_readdata,
      cart_waitcnt          => open,
      dma_eepromcount       => open,
      cart_reset            => open,

      ewram_ena             => open,
      ewram_rnw             => open,
      ewram_addr            => open,
      ewram_be              => open,
      ewram_writedata       => open,
      ewram_done            => '0',
      ewram_readdata        => (others => '0'),

      SAVE_out_Din          => open,
      SAVE_out_Dout         => ZERO64,
