      SAVE_out_be           => open,
      SAVE_out_done         => '0',
      savestate_bus_ext     => open,
      ss_wired_out_ext      => ZERO32,
      ss_wired_done_ext     => '0',

      bios_wraddr           => bios_wraddr,
      bios_wrdata           => bios_wrdata,
      bios_wr               => bios_wr,

      load_done             => open,

      KeyA                  => '0',
      KeyB                  => '0',
