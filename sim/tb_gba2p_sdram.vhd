      variable f1, f2 : integer := 0;
   begin
      if rising_edge(clk1x) then
         if (c1_px_we = '1' and c1_px_x = 239 and c1_px_y = 159) then
            f1 := f1 + 1;
            report "core1 PAINTED frame " & integer'image(f1) & " @ " & time'image(now);
         end if;
         if (c2_px_we = '1' and c2_px_x = 239 and c2_px_y = 159) then
            f2 := f2 + 1;
            report "core2 PAINTED frame " & integer'image(f2) & " @ " & time'image(now);
         end if;
      end if;
   end process;

