   -- ==================================================================
   -- hang watchdogs: every bus handshake must complete within a frame
   -- ==================================================================
   watchdogs : block
      constant HANG_CYCLES : integer := 20000; -- clk1x; any real op is < 100

      procedure dump_arb(prefix : string) is
      begin
         report prefix &
            " | extern_allow=" & std_logic'image(extern_allow) &
            " ew1(a/b)=" & std_logic'image(ewram_active) & std_logic'image(ewram_busy) &
            " ew2(a/b)=" & std_logic'image(ew2_active) & std_logic'image(ew2_busy) &
            " cart2(a/b)=" & std_logic'image(cart2_active) & std_logic'image(cart2_busy) &
            " cart_ena=" & std_logic'image(cart_ena) &
            " c1_pc=0x" & to_hstring(c1_export.pc) &
            " c2_pc=0x" & to_hstring(c2_export.pc)
            severity error;
      end procedure;
   begin

      ew1_watch : process (clk1x)
         variable cnt : integer := 0;
      begin
         if rising_edge(clk1x) then
            if (ewram_ena = '1') then cnt := 1;
            elsif (ewram_done = '1') then cnt := 0;
            elsif (cnt > 0) then
               cnt := cnt + 1;
               if (cnt = HANG_CYCLES) then
                  dump_arb("HANG core1 EWRAM addr=0x" & to_hstring(unsigned(ewram_addr)) & " rnw=" & std_logic'image(ewram_rnw));
                  report "core1 EWRAM handshake never completed" severity failure;
               end if;
            end if;
         end if;
      end process;

      ew2_watch : process (clk1x)
         variable cnt : integer := 0;
      begin
         if rising_edge(clk1x) then
            if (ew2_ena = '1') then cnt := 1;
            elsif (ew2_done = '1') then cnt := 0;
            elsif (cnt > 0) then
               cnt := cnt + 1;
               if (cnt = HANG_CYCLES) then
                  dump_arb("HANG core2 EWRAM addr=0x" & to_hstring(unsigned(ew2_addr)) & " rnw=" & std_logic'image(ew2_rnw));
                  report "core2 EWRAM handshake never completed" severity failure;
               end if;
            end if;
         end if;
      end process;

      cart1_watch : process (clk1x)
         variable cnt : integer := 0;
      begin
         if rising_edge(clk1x) then
            if (cart_ena = '1') then cnt := 1;
            elsif (cart_done = '1') then cnt := 0;
            elsif (cnt > 0) then
               cnt := cnt + 1;
               if (cnt = HANG_CYCLES) then
                  dump_arb("HANG core1 cart addr=0x" & to_hstring(unsigned(cart_addr)) & " rnw=" & std_logic'image(cart_rnw));
                  report "core1 cart handshake never completed" severity failure;
               end if;
            end if;
         end if;
      end process;

      cart2_watch : process (clk1x)
         variable cnt : integer := 0;
      begin
         if rising_edge(clk1x) then
            if (c2_cart_ena = '1') then cnt := 1;
            elsif (c2_cart_done = '1') then cnt := 0;
            elsif (cnt > 0) then
               cnt := cnt + 1;
               if (cnt = HANG_CYCLES) then
                  dump_arb("HANG core2 cart addr=0x" & to_hstring(unsigned(c2_cart_addr)) & " rnw=" & std_logic'image(c2_cart_rnw));
                  report "core2 cart handshake never completed" severity failure;
               end if;
            end if;
         end if;
      end process;

   end block;

