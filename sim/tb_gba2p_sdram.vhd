   -- SIO wire monitor: decode 16-bit multiplayer frames off the cable.
   -- Data changes on SC falling, both ends sample on rising: sample there.
   -- LSB travels first on the real wire, so frames assemble from the right.
   siomon : process (clk1x)
      variable sc_1        : std_logic := '1';
      variable bits        : integer := 0;
      variable idle        : integer := 0;
      variable parent_word : std_logic_vector(15 downto 0) := (others => '0');
      variable child_word  : std_logic_vector(15 downto 0) := (others => '0');
      variable frames      : integer := 0;
   begin
      if rising_edge(clk1x) then
         if (sc_1 = '0' and l_sc = '1') then
            parent_word := l_si_c2 & parent_word(15 downto 1); -- core 1's SO
            child_word  := l_so_c2 & child_word(15 downto 1);  -- core 2's SO
            bits := bits + 1;
            idle := 0;
            if (bits = 16) then
               frames := frames + 1;
               report "SIO frame " & integer'image(frames) &
                      ": parent " & to_hstring(parent_word) &
                      " child " & to_hstring(child_word);
               bits := 0;
            end if;
         elsif (bits > 0) then
            idle := idle + 1;
            if (idle > 5000) then -- ~300us without a clock edge: dead exchange
               report "SIO PARTIAL frame, " & integer'image(bits) & " bits"
                      severity warning;
               bits := 0;
               idle := 0;
            end if;
         end if;
         sc_1 := l_sc;
      end if;
   end process;

